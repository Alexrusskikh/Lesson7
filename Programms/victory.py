print('Примите участие в исторической викторине')


newRound = "да"
while newRound == "да":
    right_answer = 0  # верные ответы
    wrong_answer = 0  # неверные ответы
    quantity_of_question = int(input('Сколько Вам задать вопросов(от 1 до 10)?: '))
    keys = random.sample(list(all_dict), quantity_of_question)

    for key in keys:

        answer = input("Укажите дату рождения ('dd.mm.yyyy'): " + key + " \n")

        if answer == all_dict[key]:
            print("Верно!!!")
            right_answer += 1
        else:
            data_to_list = (all_dict[key]).split('.')#разбиение  даты на числа по разделителю (.)
